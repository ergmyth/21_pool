#include "ft.h"

int		main(int argc, char **argv)
{
	int c;
	int d;

	if (argc == 3)
	{
		c = ft_atoi(argv[1]);
		d = ft_atoi(argv[2]);
		rush(c, d);
	}
	if (argc != 3 || c <= 0 || d <= 0)
		ft_putchar('\n');
	return (0);
}
